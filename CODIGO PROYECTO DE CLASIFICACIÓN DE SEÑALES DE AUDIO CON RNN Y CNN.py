"""## **Modelo RNN** 🌸

🔹 Preparación de datos para el modelo RNN - RNN procesa la señal como una secuencia temporal, manteniendo memoria de los valores anteriores para entender la evolución del audio.”

En esta sección los audios se convierten en características MFCC, las cuales representan el sonido como una secuencia de valores. Esta representación es adecuada para modelos RNN porque permite analizar cómo cambia la voz a través del tiempo.
"""

def audio_to_mfcc(path, max_pad_len=130):
    audio, sr = librosa.load(path, sr=22050)
    mfcc = librosa.feature.mfcc(y=audio, sr=sr, n_mfcc=13)

    if mfcc.shape[1] < max_pad_len:
        pad_width = max_pad_len - mfcc.shape[1]
        mfcc = np.pad(mfcc, pad_width=((0,0), (0,pad_width)), mode='constant')
    else:
        mfcc = mfcc[:, :max_pad_len]

    return mfcc

""" **Conversión de audios a MFCC** son una representación numérica del audio que captura las características más importantes de la voz humana."""

X_train_rnn = np.array([audio_to_mfcc(p) for p in X_train])
