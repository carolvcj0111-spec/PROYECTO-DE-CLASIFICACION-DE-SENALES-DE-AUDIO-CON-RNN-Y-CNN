"""## 🔹**Conversión de audio a espectrograma**"""

def audio_to_melspectrogram(path, max_pad_len=70):
    audio, sr = librosa.load(path, sr=22050)
    mel = librosa.feature.melspectrogram(y=audio, sr=sr, n_mels=64)
    mel_db = librosa.power_to_db(mel, ref=np.max)

    if mel_db.shape[1] < max_pad_len:
        pad_width = max_pad_len - mel_db.shape[1]
        mel_db = np.pad(mel_db, pad_width=((0,0), (0,pad_width)), mode='constant',constant_values=-80)
    else:
        mel_db = mel_db[:, :max_pad_len]

    return mel_db

